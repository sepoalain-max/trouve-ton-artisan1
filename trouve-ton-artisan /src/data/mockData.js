/**
 * Retourne la catégorie d'un artisan
 */
export function getCategorieByArtisan(artisan) {
  const spec = specialites.find(s => s.id === artisan.specialiteId);
  if (!spec) return null;
  return categories.find(c => c.id === spec.categorieId) || null;
}

/**
 * Retourne la spécialité d'un artisan
 */
export function getSpecialiteByArtisan(artisan) {
  return specialites.find(s => s.id === artisan.specialiteId) || null;
}

